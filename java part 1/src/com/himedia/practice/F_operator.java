package com.himedia.java.practice;

public class F_operator {
    public static void exam1() {
        int a=10, b=4;

        System.out.printf("%d + %d = %d\n", a, b, a+b);
        System.out.printf("%d - %d = %d\n", a, b, a-b);
        System.out.printf("%d * %d = %d\n", a, b, a/b);
        System.out.printf("%d / %d = %d\n", a, b, a/b);
        System.out.printf("%d / %d = %f\n", a, b, a/(double)b);
        System.out.printf("%d %% %d = %d\n", a, b, a%b);
    }
}
